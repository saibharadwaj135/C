#include<stdio.h>
int main()
{
    int a[20][20],b[20][20],c[20][20],i,j,m,n;
    scanf("%d%d",&m,&n);
    for(i=0;i<m;i++)
    {
        for(j=0;j<n;j++)
        {
            scanf("%d",&a[i][j]);
        }
    }
    for(i=0;i<m;i++)
    {
        for(j=0;j<n;j++)
        {
            scanf("%d",&b[i][j]);
        }
    }
    for(i=0;i<m;i++)
    {
        for(j=0;j<n;j++)
        {
            c[i][j]=a[i][j]+b[i][j];
        }
    }
    for(i=0;i<m;i++)
    {  printf("\n");
        for(j=0;j<n;j++)
        {
            printf("%d\t",c[i][j]);
        }
    }
    

}